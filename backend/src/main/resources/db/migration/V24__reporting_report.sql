-- Responsibility: create the report table behind the Report entity (BaseCodedEntity:
-- it carries the human ticket code TAR-YYYY-NNNNNN in addition to the BaseEntity
-- shape). MUST match the entity so ddl-auto=validate passes (ARCHITECTURE.md §4;
-- PRD §10 M3, §12.1, §25.3, Appendix D).
--
-- Integrity/privacy invariants reflected here (PRD §19/§25.3/§23.5, CLAUDE.md §12):
--   * ANONYMOUS SENSITIVE FILING (D-Q1): reporter_profile_id is NULLABLE — a sensitive
--     report stores NO reporter linkage at all (strongest protection; tracked by code).
--   * CROSS-MODULE REFERENCE BY ID, not FK: reporter_profile_id, reporter_ward_id,
--     constituency_id, assigned_responder_id, duplicate_of_id are UUID publicIds of
--     OTHER modules' entities — intentionally NOT FKs, to keep the module boundary
--     clean and make anonymity a simple NULL (ARCHITECTURE.md §4.3).
--   * ROUTING DEFERRED: assigned_responder_id is a STUB — reports are created NEW and
--     never auto-assigned in this increment (see // TODO(wiring)).
--   * INTEGRITY FENCE (D18/§23.5): upvotes/followers are discovery-reach counters ONLY;
--     they must never influence routing/SLA/priority/resolution. They live here as
--     denormalised counts; nothing reads them for official routing.
--
-- Type mapping contract (entity -> column): as V2/V3/V23, plus
--   Point -> geometry(Point,4326); Boolean (nullable) -> boolean (nullable);
--   long -> bigint not null.
--
-- Forward-only; never edit once applied — add a new migration.
